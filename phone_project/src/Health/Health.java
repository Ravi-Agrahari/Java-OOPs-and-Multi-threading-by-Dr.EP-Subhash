
package Health;


public class Health {
    private int stepsWalked ;
    private int floorsClimb ; 

    public int getStepsWalked() {
        return stepsWalked;
    }

    public void setStepsWalked(int stepsWalked) {
        this.stepsWalked = stepsWalked;
    }

    public int getFloorsClimb() {
        return floorsClimb;
    }

    public void setFloorsClimb(int floorsClimb) {
        this.floorsClimb = floorsClimb;
    }
    
}
